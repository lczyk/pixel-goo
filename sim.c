void buffer_setup(void) {
    fprintf(stdout, "Setting up buffers...\n");

    // Derived buffer sizes, now that width/height are final (set in the front-end's window setup)
    density_width = width / densityBufferDownsampling + 1;
    density_height = height / densityBufferDownsampling + 1;
    repel_width = width / repelBufferDownsampling + 1;
    repel_height = height / repelBufferDownsampling + 1;
    trail_width = width / trailBufferDownsampling + 1;
    trail_height = height / trailBufferDownsampling + 1;

    // Vertex array. The point passes get their attributes from the PBOs (wired below);
    // the fullscreen-quad passes use gl_VertexID -- so the VAO needs no static buffer.
    GLuint vao;
    glGenVertexArrays(1, &vao);
    glBindVertexArray(vao);

    // Calculate size of the physics buffer
    int PBwidth = ceil(sqrt(P));
    int PBheight = ceil(P / sqrt(P));
    PB_width = PBwidth;
    PB_height = PBheight;
    fprintf(stdout, "%d points\n", P);
    // Initial density (inverse of -d): P over logical screen area, so it's comparable across
    // sim sizes / render scales regardless of whether -p or -d set the count.
    double logical_area = (double)width * height * render_scale * render_scale;
    fprintf(stdout, "Initial density: %.4g particles/logical-px^2\n", logical_area > 0 ? P / logical_area : 0.0);
    fprintf(stdout, "Physics framebuffer shape: %d x %d\n", PBwidth, PBheight);
    updateShaderBufferShape(PBwidth, PBheight);

    // PBOs sized to the full physics buffer (RG = 2 floats/texel). Filled each frame
    // by glReadPixels(FBO->PBO), then reused as GL_ARRAY_BUFFER so the point passes
    // read positions/velocities as streamed attributes instead of vertex texture fetch.
    size_t pb_bytes = (size_t)PBwidth * PBheight * 2 * sizeof(float);
    glGenBuffers(1, &pbo_pos);
    glBindBuffer(GL_PIXEL_PACK_BUFFER, pbo_pos);
    glBufferData(GL_PIXEL_PACK_BUFFER, pb_bytes, NULL, GL_DYNAMIC_COPY);
    glGenBuffers(1, &pbo_vel);
    glBindBuffer(GL_PIXEL_PACK_BUFFER, pbo_vel);
    glBufferData(GL_PIXEL_PACK_BUFFER, pb_bytes, NULL, GL_DYNAMIC_COPY);
    glBindBuffer(GL_PIXEL_PACK_BUFFER, 0);

    // Wire the PBOs as vertex attributes (position = loc 0, velocity = loc 1) on the
    // still-bound VAO, so the point passes stream them instead of vertex texture fetch.
    glBindBuffer(GL_ARRAY_BUFFER, pbo_pos);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 0, 0);
    glBindBuffer(GL_ARRAY_BUFFER, pbo_vel);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 0, 0);
    glBindBuffer(GL_ARRAY_BUFFER, 0);

    // Index buffer + CPU scratch for the tile-coherent draw order (rebuild_sort_order).
    glGenBuffers(1, &ibo);
    glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, ibo);
    glBufferData(GL_ELEMENT_ARRAY_BUFFER, (size_t)PBwidth * PBheight * sizeof(unsigned int), NULL, GL_DYNAMIC_DRAW);
    {
        int tilesW = (width + sort_tile - 1) / sort_tile;
        int tilesH = (height + sort_tile - 1) / sort_tile;
        sort_idx = (unsigned int *)malloc((size_t)P * sizeof(unsigned int));
        sort_key = (int *)malloc((size_t)P * sizeof(int));
        sort_counts = (int *)malloc(((size_t)tilesW * tilesH + 1) * sizeof(int));
        // Identity order until the first rebuild, so frame 0 still draws every particle.
        for (int i = 0; i < P; i++)
            sort_idx[i] = (unsigned int)i;
        glBufferSubData(GL_ELEMENT_ARRAY_BUFFER, 0, (size_t)P * sizeof(unsigned int), sort_idx);
    }

    // Initalise textures and the associated framebuffers
    glGenTextures(9, textures);
    glGenFramebuffers(9, framebuffers);

    // Texture 0 - Density buffer
    densityBuffer.minmag_filter = densityNearest ? GL_NEAREST : GL_LINEAR; // --dens-nearest for chunky pixels
    densityBuffer.wrap_st = GL_REPEAT;
    densityBuffer.dim = BE_1D;
    buffer_allocate(&densityBuffer, current, densityBufferIndex, density_width, density_height, NULL);
    // Scratch for the auto-headroom: read the (tiny, downsampled) density buffer back to
    // find its max each density update. Sized generously so a resize can't overflow it.
    density_readback = (float *)malloc((size_t)(density_width + 8) * (density_height + 8) * sizeof(float));
    // Seed 0 in auto mode so ema()'s seed-on-zero snaps to the FIRST measured level instead of
    // ramping from a guessed constant (that ramp showed as faint -> bright over the first ~1/attack frames).
    headroom_ema = (float)(renderHeadroom > 0.0 ? renderHeadroom : 0.0);

    // Texture 8 - Repel buffer (r = force potential, g = dispersion). Single-buffered and
    // stateless: fully cleared + re-splatted every sim_step (see the repel pass), so it
    // has no "other" half and never flips. Its own resolution knob (--repel-downsample), decoupled
    // from the density buffer -- this sets the hardness floor for the edge/exclusion/mouse repel.
    repelBuffer.minmag_filter = GL_LINEAR;
    repelBuffer.wrap_st = GL_REPEAT; // matches density/trail: velocity.frag samples it the same way
    repelBuffer.dim = BE_2D;
    buffer_allocate(&repelBuffer, current, repelBufferIndex, repel_width, repel_height, NULL);

    positionBuffer.minmag_filter = GL_NEAREST;
    positionBuffer.dim = BE_2D;
    velocityBuffer.minmag_filter = GL_NEAREST;
    velocityBuffer.dim = BE_2D;

    // Restore path (--gl-refresh): if a snapshot is queued and its dims match, upload
    // the saved particle/trail state instead of generating a fresh field, so the sim
    // continues across a GL-context recreate. NOTE: mismatched dims (only possible if
    // the surface resized between snapshot and setup) fall through to a fresh field.
    bool restoring = g_restore && g_restore->pb_w == PBwidth && g_restore->pb_h == PBheight &&
                     g_restore->tr_w == trail_width && g_restore->tr_h == trail_height;

    // Texture 1 - Position buffer 1
    // --seed makes the initial positions (and so the whole deterministic gpu evolution)
    // reproducible, which is what lets before/after dumps actually be comparable.
    const float *pos_data;
    float *positions = NULL;
    if (restoring) {
        pos_data = g_restore->pos;
    } else {
        srand(rng_seed ? (unsigned int)rng_seed : (unsigned int)time(NULL));
        float margin = (sim_width < sim_height ? sim_width : sim_height) * 0.1f;
        float noise_seed = rng_seed ? (float)rng_seed : (10 * get_time() + frand01());
        fprintf(stdout, "Generating starting positions...\n");
        int N = PBwidth * PBheight * 2;
        positions = (float *)malloc(N * sizeof(float));
        for (int i = 0; i < N; i += 2) {
            // Generate random position in unit range
            vec2 position = {frand01(), frand01()};
            vec3 p0 = {10 * position[0], 10 * position[1], noise_seed};
            vec3 p1 = {10 * position[0], 10 * position[1], noise_seed + 1};
            vec2 noise = {glm_perlin_vec3(p0), glm_perlin_vec3(p1)};
            position[0] += (float)init_warp * fmodf(noise[0], 1.0f);
            position[1] += (float)init_warp * fmodf(noise[1], 1.0f);

            // Cull position to the center of the screen
            position[0] = position[0] * (sim_width - 2 * margin) + margin;
            position[1] = position[1] * (sim_height - 2 * margin) + margin;

            positions[i] = position[0];
            positions[i + 1] = position[1];
        }
        pos_data = positions;
    }

    fprintf(stdout, "Allocating buffers...\n");
    buffer_allocate(&positionBuffer, current, positionBufferIndex1, PBwidth, PBheight, (const char *)pos_data);

    free(positions);

    // Texture 2,3,4 - position buffer 2, velocity buffer 1 and 2. On restore, velocity
    // current gets the saved velocities (else zero); the "other" halves stay zero (the
    // double buffer flips fill them within a frame).
    buffer_allocate(&positionBuffer, other, positionBufferIndex2, PBwidth, PBheight, NULL);
    buffer_allocate(&velocityBuffer, current, velocityBufferIndex1, PBwidth, PBheight,
                    restoring ? (const char *)g_restore->vel : NULL);
    buffer_allocate(&velocityBuffer, other, velocityBufferIndex2, PBwidth, PBheight, NULL);

    // Texture 5,6 - Trail double buffer (current restored from the snapshot if present)
    trailBuffer.minmag_filter = GL_NEAREST;
    trailBuffer.wrap_st = GL_REPEAT;
    trailBuffer.dim = BE_1D;
    buffer_allocate(&trailBuffer, current, trailBufferIndex1, trail_width, trail_height,
                    restoring ? (const char *)g_restore->trail : NULL);
    buffer_allocate(&trailBuffer, other, trailBufferIndex2, trail_width, trail_height, NULL);

    // Texture 7 - internal render target (rgba colour) at render resolution.
    // GL_NEAREST so the upscale to the window stays crisp/pixelated at low res.
    renderBuffer.minmag_filter = GL_NEAREST;
    renderBuffer.wrap_st = GL_CLAMP_TO_EDGE;
    renderBuffer.dim = BE_4D;
    buffer_allocate(&renderBuffer, current, renderBufferIndex, width, height, NULL);

    // The screen buffer is the window itself (the upscale target), in device px.
    screenBuffer.width = window_width;
    screenBuffer.height = window_height;

    // Carry the auto-exposure state across a restore so it doesn't re-ramp, then
    // consume the snapshot (one restore per snapshot).
    if (restoring)
        headroom_ema = g_restore->headroom_ema;
    g_restore = NULL;
}

SimSnapshot *sim_snapshot(void) {
    size_t pv = (size_t)PB_width * PB_height * 2;
    size_t tn = (size_t)trail_width * trail_height;
    SimSnapshot *s = (SimSnapshot *)calloc(1, sizeof *s);
    if (s) {
        s->pos = malloc(pv * sizeof(float));
        s->vel = malloc(pv * sizeof(float));
        s->trail = malloc(tn * sizeof(float));
    }
    if (!s || !s->pos || !s->vel || !s->trail) {
        fprintf(stderr, "goo: out of memory for sim snapshot\n");
        exit(EXIT_FAILURE);
    }
    s->pb_w = PB_width;
    s->pb_h = PB_height;
    s->tr_w = trail_width;
    s->tr_h = trail_height;
    s->headroom_ema = headroom_ema;
    // Read the *current* halves of each double buffer (what sim_step just wrote).
    glBindFramebuffer(GL_READ_FRAMEBUFFER, framebuffers[positionBuffer.current]);
    glReadPixels(0, 0, PB_width, PB_height, GL_RG, GL_FLOAT, s->pos);
    glBindFramebuffer(GL_READ_FRAMEBUFFER, framebuffers[velocityBuffer.current]);
    glReadPixels(0, 0, PB_width, PB_height, GL_RG, GL_FLOAT, s->vel);
    glBindFramebuffer(GL_READ_FRAMEBUFFER, framebuffers[trailBuffer.current]);
    glReadPixels(0, 0, trail_width, trail_height, GL_RED, GL_FLOAT, s->trail);
    glBindFramebuffer(GL_READ_FRAMEBUFFER, 0);
    return s;
}

void sim_restore(const SimSnapshot *s) { g_restore = s; }

void sim_snapshot_free(SimSnapshot *s) {
    if (!s)
        return;
    free(s->pos);
    free(s->vel);
    free(s->trail);
    free(s);
}

