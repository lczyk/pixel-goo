void sim_set_dims(int logical_w, int logical_h, int fb_w, int fb_h) {
    // The actual framebuffer (window_width/height) is in device pixels and is only
    // the final upscale target. The sim renders at render resolution (width/height)
    // = logical points / render_scale, so on hidpi/retina it renders at 1x rather
    // than paying the device-pixel fill cost. The mouse comes in logical points, so
    // remember the ratio to scale it into render space.
    window_width = fb_w;
    window_height = fb_h;
    width = (int)((float)logical_w / render_scale + 0.5f);
    height = (int)((float)logical_h / render_scale + 0.5f);
    // Sim space is logical/SIM_SCALE, decoupled from the render resolution above.
    sim_width = (int)((double)logical_w / SIM_SCALE + 0.5);
    sim_height = (int)((double)logical_h / SIM_SCALE + 0.5);
    mouse_scale = (logical_w > 0) ? (float)sim_width / (float)logical_w : 1.0f;
    dpi_scale = (logical_w > 0) ? (float)fb_w / (float)logical_w : 1.0f;
}

// On resize the sim coordinate space (sim_width/height) changes, but the particle
// positions stored in positionBuffer don't -- left as-is they stay clumped in a corner
// of the new (usually larger) field and only slowly diffuse out to fill it. Scale every
// stored position by the size ratio so the field keeps filling the window immediately.
// Readback + reupload on the cpu: resizes are rare, so the cost is irrelevant, and it
// avoids standing up a dedicated scale shader pass. velocities are left unscaled -- they
// re-equilibrate within a few frames and the visible artefact is purely positional.
static void rescale_positions(float kx, float ky) {
    int n = PB_width * PB_height;
    float *p = (float *)malloc((size_t)n * 2 * sizeof(float));
    if (!p)
        return;
    glBindFramebuffer(GL_READ_FRAMEBUFFER, positionBuffer.framebuffers[positionBuffer.current]);
    glReadPixels(0, 0, PB_width, PB_height, GL_RG, GL_FLOAT, p);
    glBindFramebuffer(GL_READ_FRAMEBUFFER, 0);
    for (int i = 0; i < n; i++) {
        p[i * 2] *= kx;
        p[i * 2 + 1] *= ky;
    }
    // keep textures[i] bound to unit i (the convention buffer_allocate sets up).
    glActiveTexture(GL_TEXTURE0 + positionBuffer.current);
    glBindTexture(GL_TEXTURE_2D, positionBuffer.textures[positionBuffer.current]);
    glTexSubImage2D(GL_TEXTURE_2D, 0, 0, 0, PB_width, PB_height, GL_RG, GL_FLOAT, p);
    free(p);
}

void sim_resize(int logical_w, int logical_h, int fb_w, int fb_h) {
    int old_sim_w = sim_width, old_sim_h = sim_height;
    sim_set_dims(logical_w, logical_h, fb_w, fb_h);
    if (old_sim_w > 0 && old_sim_h > 0 && (sim_width != old_sim_w || sim_height != old_sim_h))
        rescale_positions((float)sim_width / (float)old_sim_w, (float)sim_height / (float)old_sim_h);
    glViewport(0, 0, window_width, window_height);
    updateShaderWindowShape(width, height);
    buffer_reallocate(&renderBuffer, current, width, height);
    density_width = width / densityBufferDownsampling + 1;
    density_height = height / densityBufferDownsampling + 1;
    buffer_reallocate(&densityBuffer, current, density_width, density_height);
    // Grow the readback scratch too -- glReadPixels writes density_width*density_height
    // floats into it, so a grow-resize overflows the setup-sized buffer (same class as
    // the sort_counts realloc below). Same generous +8 padding as buffer_setup.
    density_readback = (float *)realloc(density_readback, (size_t)(density_width + 8) * (density_height + 8) * sizeof(float));
    // Repel buffer has its own resolution knob (--repel-downsample); recompute + realloc.
    repel_width = width / repelBufferDownsampling + 1;
    repel_height = height / repelBufferDownsampling + 1;
    buffer_reallocate(&repelBuffer, current, repel_width, repel_height);
    trail_width = width / trailBufferDownsampling + 1;
    trail_height = height / trailBufferDownsampling + 1;
    buffer_reallocate(&trailBuffer, current, trail_width, trail_height);
    buffer_reallocate(&trailBuffer, other, trail_width, trail_height);
    // sort_counts is sized by tile count, which grows with width/height -- resize it too,
    // else rebuild_sort_order writes past the end (sort_idx/sort_key are P-sized, stable).
    {
        int tilesW = (width + sort_tile - 1) / sort_tile;
        int tilesH = (height + sort_tile - 1) / sort_tile;
        sort_counts = (int *)realloc(sort_counts, ((size_t)tilesW * tilesH + 1) * sizeof(int));
    }
}

// Push the static (per-run) uniforms after shaders + buffers exist. The per-frame
// uniforms (mouse, buffer indices, epoch) are set inside sim_step.
static void sim_apply_static_uniforms(void) {
    shader_set_uniform_int(&screenShader, "density_buffer", densityBuffer.current);
    shader_set_uniform_float(&screenShader, "cull_amount", (float)cullAmount);
    shader_set_uniform_float(&screenShader, "render_headroom", (float)renderHeadroom);
    shader_set_uniform_float(&screenShader, "render_gamma", (float)renderGamma);
    shader_set_uniform_int(&densityShader, "density_buffer_downsampling", densityBufferDownsampling);
    // NOTE: the density field accumulates count-per-texel, so its raw value still scales with
    // render_scale (finer buffer = lower peak), and the physics reads it -> -r has a residual
    // effect on the dynamics via field discretisation. Naive texel-area normalisation overcorrects
    // (weaker deposit kills clustering nonlinearly), so it's left alone: -r is a fidelity knob, and
    // the coordinate refactor already makes world size / blob scale / density -r-independent.
    shader_set_uniform_float(&densityShader, "density_alpha", densityAlpha * densitySubsample);
    // kernelRadius is in sim px; the density buffer is at render res, so convert sim->render-px.
    // At the default render_scale == SIM_SCALE the factor is 1 (splat size unchanged).
    shader_set_uniform_float(&densityShader, "kernel_radius", kernelRadius * (float)(SIM_SCALE / render_scale));
    shader_set_uniform_float(&velocityShader, "drag_coefficient", dragCoefficient);
    shader_set_uniform_float(&velocityShader, "dither_coefficient", ditherCoefficient);
    shader_set_uniform_float(&velocityShader, "dither_density_gain", (float)ditherDensityGain);
    shader_set_uniform_float(&velocityShader, "dither_ortho", (float)ditherOrtho);
    shader_set_uniform_int(&velocityShader, "legacy_wedge", legacyWedge ? 1 : 0);
    shader_set_uniform_float(&velocityShader, "density_force", (float)densityForce);
    shader_set_uniform_float(&velocityShader, "trail_force", (float)trailForce);
    shader_set_uniform_float(&velocityShader, "repel_coefficient", (float)repel);
    shader_set_uniform_float(&velocityShader, "reach", (float)reach);
    shader_set_uniform_float(&velocityShader, "anti_stick", (float)antiStick);
    shader_set_uniform_float(&velocityShader, "trail_reach", (float)trailReach);
    shader_set_uniform_int(&velocityShader, "density_buffer", densityBuffer.current);
    // repelBuffer is single-buffered (no ping-pong), so its texture unit never changes.
    shader_set_uniform_int(&velocityShader, "repel_buffer", repelBuffer.current);
    // Trail spread-deposit (--trail-every N): the trail decays EVERY frame (smooth fade,
    // no frozen-field discontinuity), but each frame deposits only a rotating 1/N subset
    // of the trail particles. With the per-point intensity scaled by N, the per-frame
    // deposit total -- and so the field the physics sees -- is unchanged, while the
    // expensive point scatter drops ~N x. Decay alpha stays per-frame (no compounding).
    shader_set_uniform_float(&copyShader, "alpha", decay_alpha(trailTau, 1));
    shader_set_uniform_int(&trailShader, "trail_buffer_downsampling", trailBufferDownsampling);
    shader_set_uniform_float(&trailShader, "trail_intensity", trailIntensity * trailSubsample * trail_every);
    shader_set_uniform_float(&trailShader, "velocity_floor", trailVelocityFloor);
    // sim px -> render-px (trail buffer is at render res); factor 1 at render_scale == SIM_SCALE.
    shader_set_uniform_float(&trailShader, "kernel_radius", trailRadius * (float)(SIM_SCALE / render_scale));
}

void sim_setup(void) {
    // Density is the default oracle for P: unless -p was given explicitly, derive P from the
    // logical (screen) area now that the front-end has set width/height. logical area =
    // width*height*render_scale^2 (width = logical/render_scale), so the count is independent of
    // both sim size and -r. Must run before buffer_setup, which consumes P.
    if (!p_given) {
        double logical_area = (double)width * height * render_scale * render_scale;
        P = (int)(init_density * logical_area + 0.5);
        if (P < 1)
            P = 1;
    }

    shader_setup();
    buffer_setup();
    sim_apply_static_uniforms();
}

// One physics + render frame. Runs the velocity / position / density / trail / screen
// passes into renderBuffer (render resolution). Does NOT present -- the front-end calls
// sim_present (or does its own readback) afterwards.
void sim_step(int epoch_counter, const float mouse_position[2], const float mouse_velocity[2]) {
    double _tp = 0;
    if (profile) {
        glFinish();
        _tp = get_time();
    }

    // Interaction pass: fully clear + re-splat the force-potential (r) / dispersion (g) field
    // from scratch every frame -- bounding-rect edges, exclusion rects (--exclusions), and the
    // mouse. Single-buffered and stateless (no flip), so it's just a plain overwrite. Must run
    // BEFORE the velocity pass, which reads the fully-written result for this frame.
    shader_use(&repelShader);
    shader_set_uniform_vec(&repelShader, "mouse_position", 2, (float *)mouse_position);
    shader_set_uniform_vec(&repelShader, "mouse_velocity", 2, (float *)mouse_velocity);
    shader_set_uniform_vec(&repelShader, "buffer_shape", 2, (float[]){(float)repel_width, (float)repel_height});
    buffer_bind(&repelBuffer, current);
    buffer_update(&repelBuffer);

    // Velocity pass
    shader_use(&velocityShader);
    shader_set_uniform_vec(&velocityShader, "mouse_position", 2, (float *)mouse_position);
    shader_set_uniform_vec(&velocityShader, "mouse_velocity", 2, (float *)mouse_velocity);
    // repel buffer's resolution -- velocity.frag needs it to size the one-texel step of the
    // local-gradient repel (a hard-edged central difference, not the reach-wide VDI).
    shader_set_uniform_vec(&velocityShader, "buffer_shape", 2, (float[]){(float)repel_width, (float)repel_height});
    shader_set_uniform_int(&velocityShader, "position_buffer", positionBuffer.current);
    shader_set_uniform_int(&velocityShader, "velocity_buffer", velocityBuffer.current);
    shader_set_uniform_int(&velocityShader, "trail_buffer", trailBuffer.current);
    shader_set_uniform_int(&velocityShader, "epoch_counter", epoch_counter);
    buffer_bind(&velocityBuffer, other);
    buffer_update(&velocityBuffer);
    buffer_flip(&velocityBuffer);
    LAP(0);

    // Position pass
    shader_use(&positionShader);
    shader_set_uniform_int(&positionShader, "position_buffer", positionBuffer.current);
    shader_set_uniform_int(&positionShader, "velocity_buffer", velocityBuffer.current); // read from updated velocity buffer
    buffer_bind(&positionBuffer, other);
    buffer_update(&positionBuffer);
    buffer_flip(&positionBuffer);
    LAP(1);

    // Copy the fresh position + velocity textures into PBOs (FBO -> GL_PIXEL_PACK_BUFFER,
    // stays on-gpu). Those PBOs are bound as the point passes' vertex attributes, and
    // pbo_pos is also CPU-mapped to build the tile-coherent draw order.
    glBindFramebuffer(GL_READ_FRAMEBUFFER, positionBuffer.framebuffers[positionBuffer.current]);
    glBindBuffer(GL_PIXEL_PACK_BUFFER, pbo_pos);
    glReadPixels(0, 0, PB_width, PB_height, GL_RG, GL_FLOAT, 0);
    glBindFramebuffer(GL_READ_FRAMEBUFFER, velocityBuffer.framebuffers[velocityBuffer.current]);
    glBindBuffer(GL_PIXEL_PACK_BUFFER, pbo_vel);
    glReadPixels(0, 0, PB_width, PB_height, GL_RG, GL_FLOAT, 0);
    glBindBuffer(GL_PIXEL_PACK_BUFFER, 0);
    glBindFramebuffer(GL_READ_FRAMEBUFFER, 0);
    LAP(6);

    // Rebuild the tile-coherent draw order from the freshly read-back positions.
    if (sort_every > 0 && epoch_counter % sort_every == 0) {
        double s0 = get_time();
        rebuild_sort_order();
        pass_ms[7] = ema(pass_ms[7], (get_time() - s0) * 1000.0, 0.1);
    }

    // Density buffer pass. Amortised: the density field is a slow-moving snapshot,
    // and the buffer isn't flipped, so on skipped frames it just keeps the last
    // rebuild (1..N-1 frame stale -- negligible to the velocity integral that reads
    // it). buffer_bind clears, so skip the whole block (not just the draw).
    if (dens_every <= 1 || epoch_counter % dens_every == 0) {
        shader_use(&densityShader);
        shader_set_uniform_int(&densityShader, "density_buffer_downsampling", densityBufferDownsampling);
        buffer_bind(&densityBuffer, current);
        glBlendFunc(GL_ONE, GL_ONE); // additive density accumulation (unbounded)
        buffer_update_n(&densityBuffer, P / densitySubsample);
        glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA); // restore the global blend

        // Auto-headroom: read the fresh (tiny) density field back, take a high
        // percentile (robust to outlier hot-spots, unlike the raw max), and EMA it --
        // the envelope follower that sets the colormap's makeup gain.
        if (renderHeadroom <= 0.0) {
            glBindFramebuffer(GL_READ_FRAMEBUFFER, framebuffers[densityBuffer.current]);
            glReadPixels(0, 0, density_width, density_height, GL_RED, GL_FLOAT, density_readback);
            glBindFramebuffer(GL_READ_FRAMEBUFFER, 0);
            int n = density_width * density_height;
            qsort(density_readback, n, sizeof(float), cmp_float);
            int k = (int)(headroomPct * (n - 1));
            if (k < 0)
                k = 0;
            else if (k >= n)
                k = n - 1;
            float level = density_readback[k];
            float target = level * (float)headroomMargin;
            // Asymmetric follower: rising target (scene getting denser) closes fast to avoid
            // clipping; falling target (opening up) brightens slowly so it doesn't pop.
            double a = (headroom_ema > 0.0f && target < headroom_ema) ? headroomRelease : headroomAttack;
            headroom_ema = (float)ema(headroom_ema, target, a);
        }
    }
    LAP(2);

    // Trail buffer. Decay every frame (smooth), but deposit only a rotating 1/N
    // subset of the trail particles (--trail-every N), cycling the contiguous block
    // by frame. Particle ids aren't spatially sorted, so each block is a fair random
    // subset; over N frames all contribute. No frozen field -> no discontinuity.
    buffer_bind(&trailBuffer, other);

    shader_use(&copyShader); // First pass (decay the previous trail, every frame)
    shader_set_uniform_float(&copyShader, "alpha", decay_alpha(trailTau, 1));
    shader_set_uniform_int(&copyShader, "source_buffer", trailBuffer.current);
    buffer_update(&trailBuffer);

    shader_use(&trailShader); // Second pass (deposit this frame's rotating subset)
    int trail_total = P / trailSubsample;
    int trail_block = trail_every <= 1 ? trail_total : trail_total / trail_every;
    int trail_start = trail_every <= 1 ? 0 : (epoch_counter % trail_every) * trail_block;
    glDrawArrays(GL_POINTS, trail_start, trail_block);
    buffer_flip(&trailBuffer);
    LAP(3);

    // Screen rendering pass -> internal render target (render resolution)
    buffer_bind(&renderBuffer, current);

    shader_use(&screenShader);
    // Headroom: auto-tracked envelope (renderHeadroom == 0) or the fixed override.
    shader_set_uniform_float(&screenShader, "render_headroom",
                             renderHeadroom > 0.0 ? (float)renderHeadroom : headroom_ema);
    // Draw in tile-coherent order via the index buffer (ibo stays bound in the VAO).
    glDrawElements(GL_POINTS, P, GL_UNSIGNED_INT, 0);

    // Debug overlay: composite flat translucent-green fills into the render target (so they show
    // up in --dump/--headless which read renderBuffer, and regardless of particle density). Gated
    // on the C-side flags -> a normal run pays nothing. renderBuffer is still bound and blend is on
    // (SRC_ALPHA over). Fullscreen quad via gl_VertexID; the bound VAO needs no attributes.
    //  - exclusions_debug: fill each exclusion rect (rects live on the shader from push_exclusion_rects).
    //  - mouse_debug: fill the mouse repel-interaction disc. Radius is the same speed-scaled clamp the
    //    physics uses (repel.frag: clamp(speed*3, 30, 75) sim px -- constants mirrored here for the
    //    viz). sim px -> render px is * SIM_SCALE/render_scale; sim y is top-origin, gl_FragCoord is
    //    bottom-origin, so y flips. A parked mouse sits far off-screen, so its disc covers nothing.
    //  - edge_debug: fill the bounding-edge repel bands (render_shape lets the shader size them).
    //  - mouse_debug also draws the opaque cursor dot + a motion line from last frame's cursor
    //    position (mouse_prev = current - per-frame velocity) to the current one.
    if (exclusions_debug || mouse_debug || edge_debug) {
        shader_use(&debugShader);
        shader_set_uniform_int(&debugShader, "draw_exclusions", exclusions_debug ? 1 : 0);
        shader_set_uniform_int(&debugShader, "draw_mouse", mouse_debug ? 1 : 0);
        shader_set_uniform_int(&debugShader, "draw_edges", edge_debug ? 1 : 0);
        shader_set_uniform_vec(&debugShader, "render_shape", 2, (float[]){(float)width, (float)height});
        // repel buffer's own resolution -- lets the overlay snap to that (downsampled) texel
        // grid, so it shows the ACTUAL coarse field the physics samples, not the ideal shape.
        shader_set_uniform_vec(&debugShader, "buffer_shape", 2, (float[]){(float)repel_width, (float)repel_height});
        float mspeed = sqrtf(mouse_velocity[0] * mouse_velocity[0] + mouse_velocity[1] * mouse_velocity[1]);
        // ease-out radius, mirrors repel.frag/velocity.frag: fast rise at low speed, plateau at max.
        float mt = fminf(mspeed / 20.0f, 1.0f);
        float mrad_sim = 30.0f + (75.0f - 30.0f) * (1.0f - (1.0f - mt) * (1.0f - mt));
        float k = (float)(SIM_SCALE / render_scale);
        shader_set_uniform_vec(&debugShader, "mouse_center", 2,
                               (float[]){mouse_position[0] * k, (float)height - mouse_position[1] * k});
        shader_set_uniform_vec(&debugShader, "mouse_prev", 2,
                               (float[]){(mouse_position[0] - mouse_velocity[0]) * k,
                                         (float)height - (mouse_position[1] - mouse_velocity[1]) * k});
        shader_set_uniform_float(&debugShader, "mouse_radius", mrad_sim * k);
        glDrawArrays(GL_TRIANGLE_STRIP, 0, 4);
    }
    LAP(4);
}

void sim_present(void) {
    // Upscale pass: nearest-sample the render target onto the window. Blending
    // is off -- the render target already holds particles composited over black,
    // so this is a straight opaque blit (chunky when render_scale > 1).
    buffer_bind(&screenBuffer, screen);
    glDisable(GL_BLEND);
    shader_use(&upscaleShader);
    shader_set_uniform_int(&upscaleShader, "source_buffer", renderBuffer.current);
    buffer_update(&screenBuffer);
    glEnable(GL_BLEND);
}
